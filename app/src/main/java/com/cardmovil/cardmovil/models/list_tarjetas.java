    public void setTar_Desc(String tar_Desc) {
        Tar_Desc = tar_Desc;
    }

    public String getTar_NumTarjeta() {
        return Tar_NumTarjeta;
    }

    public void setTar_NumTarjeta(String tar_NumTarjeta) {
        Tar_NumTarjeta = tar_NumTarjeta;
    }

